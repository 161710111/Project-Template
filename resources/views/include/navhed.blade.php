
Candrabirawa (Candabirawa)


 
<p>CANDRABIRAWA, yang oleh sebagian dalang disebut Candabirawa, dalam dunia pewayangan adalah sejenis ilmu yang hanya dimiliki oleh Begawan Bagaspati dan Prabu Salyapati. Dengan ilmu penguasaan Candrabirawa, Bagaspati maupun Salya dapat mendatangkan sebangsa jin raksasa untuk mengawal dan membantunya dalam suatu pertempuran. Bila jin raksasa kerdil itu dipukul atau diserang, ia akan membelah diri menjadi dua.</p>
<br>
Jika diserang lagi, ia membelah diri lagi menjadi empat, begitu seterusnya, sehingga jumlahnya terus meningkat sesuai dengan deret ukur.
<br>

<p>
Candrabirawa pada mulanya adalah selongsong kulit Batara Antaboga, yang berganti kulit setiap 1000 tahun sekali. Dari selongsong kulit itu Batara Guru menciptakannya menjadi makhluk naga yang mengerikan, lalu disuruh menyerang Begawan Bagaspati. Namun, pendeta berujud raksasa itu dapat mengalahkan Candrabirawa sehingga akhirnya makhluk itu menabdi kepadanya. Dalam perang tanding itu, dengan kesaktiannya Bagaspati mengubah wujud Candrabirawa yang semula berupa naga, menjadi raksasa kerdil yang maya.
</p>
